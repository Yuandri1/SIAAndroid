package com.example.uasmobile;

public class ListDataPengadaan {
    private String Kode, TipePengada, Nama, Quantity, Harga, Deskripsi, AkunDebet, Debet, AkunKredit, Kredit;

    public ListDataPengadaan() {
    }

    public String getKode() {
        return Kode;
    }

    public void setKode(String kode) {
        Kode = kode;
    }

    public String getTipePengada() {
        return TipePengada;
    }

    public void setTipePengada(String tipePengada) {
        TipePengada = tipePengada;
    }

    public String getNama() {
        return Nama;
    }

    public void setNama(String nama) {
        Nama = nama;
    }

    public String getQuantity() {
        return Quantity;
    }

    public void setQuantity(String quantity) {
        Quantity = quantity;
    }

    public String getHarga() {
        return Harga;
    }

    public void setHarga(String harga) {
        Harga = harga;
    }

    public String getDeskripsi() {
        return Deskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        Deskripsi = deskripsi;
    }

    public String getAkunDebet() {
        return AkunDebet;
    }

    public void setAkunDebet(String akunDebet) {
        AkunDebet = akunDebet;
    }

    public String getDebet() {
        return Debet;
    }

    public void setDebet(String debet) {
        Debet = debet;
    }

    public String getAkunKredit() {
        return AkunKredit;
    }

    public void setAkunKredit(String akunKredit) {
        AkunKredit = akunKredit;
    }

    public String getKredit() {
        return Kredit;
    }

    public void setKredit(String kredit) {
        Kredit = kredit;
    }
}

